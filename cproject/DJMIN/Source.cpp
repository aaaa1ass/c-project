void check_bottons(int time, char key, int* score)
{
	int checked = 0;
	char bottons[4] = { 'a', 's', ';', '\'' };

	for (int i = 0; i < 4; i++)
	{
		if (key == bottons[i])
		{
			for (int j = 5; j >= -1; j--)
			{
				if (block_array_1[time - 26 - j][i] == 1)
				{
					block_array_1[time - 26 - j][i] = 0;
					if (j == 5 || j == -1)
					{
						*score += 1;
						accuracy[i] = GOOD;
					}
					else if (j == 4 || j == 0)
					{
						*score += 2;
						accuracy[i] = NICE;
					}
					else
					{
						*score += 3;
						accuracy[i] = PERFECT;
					}
						break;
				}
			}
		}
	}
}
